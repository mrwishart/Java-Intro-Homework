public class Calculator {

    public int add(int numA, int numB) {
        return numA + numB;
    }

    public int subtract(int numA, int numB) {
        return numA - numB;
    }

    public int multiply(int numA, int numB) {
        return numA * numB;
    }

    public double divide(int numA, int numB) {
        return (double)numA / numB;
    }

}
